package leo.shared;

// imports

public class LoginResponse {

    /////////////////////////////////////////////////////////////////
    // Constants
    /////////////////////////////////////////////////////////////////
    public static final int LOGIN_SUCCESSFUL = 0;
    public static final int CREATE_SUCCESSFUL = 1;
    public static final int FAIL_WRONG_PASSWORD = 2;
    public static final int FAIL_ACCOUNT_EXISTS = 3;
    public static final int FAIL_NOT_EXIST = 4;
    public static final int FAIL_INCORRECT_DATA = 5;
    public static final int FAIL_ALREADY_LOGGED_IN = 6;
    public static final int FAIL_OLD_VERSION = 7;
    private static final long serialVersionUID = 1L;

    //non server responses
    public static final int SSL_ERROR = 32;
    public static String msg = "";


    /////////////////////////////////////////////////////////////////
    // Properties
    /////////////////////////////////////////////////////////////////
    private int response = -1;
    private int chatID = -1;


    /////////////////////////////////////////////////////////////////
    // Constructor
    /////////////////////////////////////////////////////////////////
    public LoginResponse(int newResponse) {
        response = newResponse;
    }


    /////////////////////////////////////////////////////////////////
    // Constructor
    /////////////////////////////////////////////////////////////////
    public LoginResponse(int newResponse, int newChatID) {
        response = newResponse;
        chatID = newChatID;
    }

    /////////////////////////////////////////////////////////////////
    // Constructor
    /////////////////////////////////////////////////////////////////
    public LoginResponse(String newMsg) {
        response = SSL_ERROR;
        msg = newMsg;
    }

    /////////////////////////////////////////////////////////////////
    // Constructor
    /////////////////////////////////////////////////////////////////
    public LoginResponse(int newResponse, int newChatID, String newMsg) {
        response = newResponse;
        chatID = newChatID;
        msg = newMsg;
    }

    /////////////////////////////////////////////////////////////////
    // Get the status
    /////////////////////////////////////////////////////////////////
    public int getResponse() {
        return response;
    }


    /////////////////////////////////////////////////////////////////
    // Get the chat id
    /////////////////////////////////////////////////////////////////
    public int getChatID() {
        return chatID;
    }

    public String getMsg() {
        return msg;
    }    
}
